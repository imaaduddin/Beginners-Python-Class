# Operators: +, -, /, *, %, **, //

# Addition
numbers = 23 + 10
print(numbers)

# Subtraction
numbers2 = 90 - 15
print(numbers2)

# Multiply
numbers3 = 9 * 9
print(numbers3)

# Division
numbers4 = 10 / 5
print(numbers4)

# Exponent
numbers5 = 5 ** 2 # 5 * 5
print(numbers5)

# Floor Division 
numbers6 = 8 // 5
print(numbers6)

# Modulo 
numbers7 = 12 % 4
print(numbers7)

numbers8 = 15 % 2
print(numbers8)

