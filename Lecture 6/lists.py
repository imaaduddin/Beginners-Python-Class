# Lists are used to store multiple items in a single variable.

"""
Lists are one of 4 built-in data types in Python used to store
collections of data, the other 3 are Tuple, Set, and Dictionary. Each
has a different properties and usages. 
"""

sports_list = ['football', 'basketball', 'baseball', 'soccer', 'tennis', 'volleyball'] # this is a list 

print(sports_list[2]) # this prints out baseball

print(len(sports_list)) # prints out the length of the list
