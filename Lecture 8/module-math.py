# Math Module 

import math

# pi

my_num = math.pi
new_num = 2 * my_num
print(new_num)

# Square Root 

square_root = int(math.sqrt(81))
print(square_root)

new_num = 2 * square_root
print(new_num)

# Exponent/Power 
my_variable = pow(5, 3)
print(my_variable)

# Absolute Value
my_var = abs(-2.35)
print(my_var)

# Minimum Number 
y = min(2, 5, 7, 9)
print(y)

# Maximum 
z = max(4, 6, 8, 10)
print(z)